import SwiftUI

struct PrimaryActionButton: View {
    let title: String
    let icon: String?
    let action: () -> Void

    init(title: String, icon: String? = nil, action: @escaping () -> Void) {
        self.title = title
        self.icon = icon
        self.action = action
    }

    var body: some View {
        PremiumButton(title: title, icon: icon, action: action)
    }
}
